#Division, Floor Division, and the Modulo Operator
print(15 / 3)
print(14 / 3)
print(14 // 3) # floor division chop off all decimal; int type

print(-14 // 3, " -14/3 floor division")
print(14 % 3, "Modulo, remainder")
print("15/3 remainder is", 15 % 3)
print("16/3 remainder is", 16 % 3)

#smaller number on the left
print(1 % 3)
print(2 % 3)
print(3 % 3)
